def _apply_run_font(run) -> None:
    """为单个文本 run 设置中文宋体、英文 Times New Roman。"""
    run.font.name = EN_FONT
    rfonts = run._element.get_or_add_rPr().get_or_add_rFonts()
    rfonts.set(qn("w:ascii"), EN_FONT)
    rfonts.set(qn("w:hAnsi"), EN_FONT)
    rfonts.set(qn("w:eastAsia"), CN_FONT)


def _apply_document_fonts(document: Document) -> None:
    """统一设置全文（含表格）字体：中文宋体、英文 Times New Roman。"""
    normal_style = document.styles["Normal"]
    normal_style.font.name = EN_FONT
    normal_style.element.get_or_add_rPr().get_or_add_rFonts().set(qn("w:eastAsia"), CN_FONT)
    for paragraph in document.paragraphs:
        for run in paragraph.runs:
            _apply_run_font(run)
    for table in document.tables:
        for row in table.rows:
            for cell in row.cells:
                for paragraph in cell.paragraphs:
                    for run in paragraph.runs:
                        _apply_run_font(run)


def _add_heading(document: Document, text: str) -> None:
    """添加加粗的二级小节标题。"""
    paragraph = document.add_paragraph()
    run = paragraph.add_run(text)
    run.bold = True
    run.font.size = Pt(12)


